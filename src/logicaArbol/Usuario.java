public class Usuario {
    private int clave, claveAux;
    private String nombre;

    public int getClave() {
        return clave;
    }

    public void setClave(int clave) {
        this.clave = clave;
    }

    public int getClaveAux() {
        return claveAux;
    }

    public void setClaveAux(int claveAux) {
        this.claveAux = claveAux;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
